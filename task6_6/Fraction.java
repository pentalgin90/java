package task6_6;

public class Fraction{
	private int ch;
	private int zn;
	
	public Fraction() {}
	
	public Fraction(int ch, int zn) {
		this.ch = ch;
		this.zn = zn;
	}
	
	public int getCh() {
		return ch;
	}

	public void setCh(int ch) {
		this.ch = ch;
	}

	public int getZn() {
		return zn;
	}

	public void setZn(int zn) {
		this.zn = zn;
	}

	
	
}
